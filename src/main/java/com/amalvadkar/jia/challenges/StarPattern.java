package com.amalvadkar.jia.challenges;

public class StarPattern {

    /*

          Write a program that displays the following pattern:

          ***
         *****
        *******
         *****
          ***
           *
     */

    public static void main(String[] args) {
        System.out.println("   *   ");
        System.out.println("  ***  ");
        System.out.println(" ***** ");
        System.out.println("*******");
        System.out.println(" ***** ");
        System.out.println("  ***  ");
        System.out.println("   *   ");
    }
}
